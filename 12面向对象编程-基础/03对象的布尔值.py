# 以下使用bool() 为 False
print(bool(False))
print(bool(0))
print(bool(None))
print(bool(''))
print(bool([]))  # 空列表
print(bool(()))  # 空元组
print(bool({}))  # 空字典
print(bool(set()))  # 空集合

