"""
函数的必要性


定义函数使用关键字 def 后跟函数名与括号内的形参列表。函数语句从下一行开始

"""


def max(num1, num2):
    if num1 > num2:
        return num1
    else:
        return num2


print(max(3, 2))
print(max(31, 211))



